	@Keyword
	def addNewDocumentTemplates() {
		WebUI.click(findTestObject('Object Repository/Home Page/Sidebar/button_Settings'))
		WebUI.click(findTestObject('Object Repository/Settings/Breadcumbs/button_My company'))
		WebUI.click(findTestObject('Object Repository/Settings/Document templates Settings/button_Document templates'))
		WebUI.waitForElementVisible(findTestObject('Object Repository/Settings/Dashboard/first input in dashboard'),0)
		WebUI.click(findTestObject('Object Repository/Settings/Dashboard/header/button_Add'))
		WebUI.waitForElementVisible(findTestObject('Object Repository/Settings/Document templates Settings/input_Reference'), 0)
		WebUI.setText(findTestObject('Object Repository/Settings/Document templates Settings/input_Reference'), 'test')
		WebUI.setText(findTestObject('Object Repository/Settings/Document templates Settings/input_Label'), 'test')
		WebUI.click(findTestObject('Object Repository/Settings/Document templates Settings/input_Document Types'))
		WebUI.sendKeys(findTestObject('Object Repository/Settings/Document templates Settings/input_Document Types'),Keys.chord(Keys.ENTER))
		WebUI.click(findTestObject('Object Repository/Settings/Document templates Settings/input_Module'))
		WebUI.sendKeys(findTestObject('Object Repository/Settings/Document templates Settings/input_Module'),Keys.chord(Keys.ENTER))
		WebUI.click(findTestObject('Object Repository/Settings/Document templates Settings/select_Generated by'))
		WebUI.sendKeys(findTestObject('Object Repository/Settings/Document templates Settings/select_Generated by'),Keys.chord(Keys.DOWN))
		WebUI.sendKeys(findTestObject('Object Repository/Settings/Document templates Settings/select_Generated by'),Keys.chord(Keys.ENTER))
		WebUI.click(findTestObject('Object Repository/Settings/Document templates Settings/input_Counter'))
		WebUI.sendKeys(findTestObject('Object Repository/Settings/Document templates Settings/input_Counter'),Keys.chord(Keys.ENTER))
		WebUI.uploadFile(findTestObject('Object Repository/Settings/Document templates Settings/input_Choose file to Upload'), RunConfiguration.getProjectDir() + '/Data Files/Upload File/DDT EU.docx')
		WebUI.waitForElementVisible(findTestObject('Object Repository/Settings/Document templates Settings/document uploaded'),0)
		WebUI.verifyElementVisible(findTestObject('Object Repository/Settings/Document templates Settings/document uploaded'))
		WebUI.click(findTestObject('Object Repository/Settings/Modal/button_Save'))
		WebUI.waitForElementVisible(findTestObject('Object Repository/Growl Notification/flash_Notice_Operation successfully completed'), 0,FailureHandling.CONTINUE_ON_FAILURE)
		WebUI.verifyElementVisible(findTestObject('Object Repository/Growl Notification/flash_Notice_Operation successfully completed'),FailureHandling.CONTINUE_ON_FAILURE)
		WebUI.waitForElementVisible(findTestObject('Object Repository/Settings/Dashboard/first input in dashboard'),0)
		WebUI.setText(findTestObject('Object Repository/Settings/Dashboard/first input in dashboard'),'test')
		WebUI.delay(3)
		WebUI.verifyElementVisible(findTestObject('Object Repository/Settings/Dashboard/first checkbox of the list'))
	}

